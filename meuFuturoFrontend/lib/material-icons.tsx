export type IconName = keyof typeof IconMapping;

// Componente de ícone Material seguindo WCAG 2.2 AA
interface MaterialIconProps {
  name: IconName;
  size?: number | string;
  className?: string;
  'aria-label'?: string;
  'aria-hidden'?: boolean;
  role?: string;
  fill?: boolean;
  weight?: 100 | 200 | 300 | 400 | 500 | 600 | 700;
  grade?: -25 | 0 | 200;
  opticalSize?: 20 | 24 | 40 | 48;
  color?: string;
  onClick?: () => void;
  onKeyDown?: (e: React.KeyboardEvent) => void;
  tabIndex?: number;
  disabled?: boolean;
}

export const MaterialIcon: React.FC<MaterialIconProps> = ({
  name,
  size = 24,
  className = '',
  'aria-label': ariaLabel,
  'aria-hidden': ariaHidden = false,
  role,
  fill = false,
  weight = 400,
  grade = 0,
  opticalSize = 24,
  color,
  onClick,
  onKeyDown,
  tabIndex = 0,
  disabled = false,
}) => {
  const iconName = IconMapping[name as keyof typeof IconMapping];
  
  if (!iconName) {
    console.warn(`Icon "${name}" not found in mapping`);
    return null;
  }

  const handleKeyDown = (e: React.KeyboardEvent) => {
    if (onKeyDown) {
      onKeyDown(e);
    }
    
    // Suporte a teclado para acessibilidade (WCAG 2.2 AA)
    if (onClick && (e.key === 'Enter' || e.key === ' ')) {
      e.preventDefault();
      onClick();
    }
  };

  const iconStyle = {
    fontSize: typeof size === 'number' ? `${size}px` : size,
    color: color,
    fontVariationSettings: `'FILL' ${fill ? 1 : 0}, 'wght' ${weight}, 'GRAD' ${grade}, 'opsz' ${opticalSize}`,
  };

  const baseClasses = [
    'material-symbols-outlined',
    'inline-flex',
    'items-center',
    'justify-center',
    'select-none',
    'transition-colors',
    'duration-200',
    disabled ? 'opacity-50 cursor-not-allowed' : 'cursor-pointer',
    className,
  ].filter(Boolean).join(' ');

  return (
    <span
      className={baseClasses}
      style={iconStyle}
      aria-label={ariaLabel}
      aria-hidden={ariaHidden}
      role={role || (onClick ? 'button' : undefined)}
      tabIndex={onClick && !disabled ? tabIndex : undefined}
      onClick={disabled ? undefined : onClick}
      onKeyDown={handleKeyDown}
      data-icon={name}
      data-material-icon={iconName}
    >
      {iconName}
    </span>
  );
};

// Hook para usar ícones com acessibilidade
export const useMaterialIcon = () => {
  const getIcon = (name: IconName, props?: Partial<MaterialIconProps>) => {
    return <MaterialIcon name={name} {...props} />;
  };

  return { getIcon, MaterialIcon };
};

